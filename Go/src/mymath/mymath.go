package mymath

func Add(a int, b int) int {
	summ := a + b
	return summ
}

func Sub(a int, b int) int {
	summ := a - b
	return summ
}

func Multiple(a int, b int) int {
	summ := a * b
	return summ
}

func Divide(a int, b int) int {
	summ := a / b
	return summ
}

func Remaining(a int, b int) int {
	summ := a % b
	return summ
}
